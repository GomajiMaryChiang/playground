<h2 id="pip-23" class="t-main"><i class="i-pip"></i>公司治理 - 獨立董事與內部稽核主管及會計師之溝通情形</h2>
<div class="TermBox">
    <ol>
        <li style="list-style: auto;">每月編製「稽核報告彙總表」，連同稽核報告影本呈送各審計委員查閱。</li>
        <li style="list-style: auto;">內部稽核主管每季定期於審計委員會議中進行內部稽核報告與獨立董事進行溝通，對於稽核業務執行情形、查核缺失改善追蹤情形及其成效皆已充分溝通。</li>
        <li style="list-style: auto;">簽證會計師至少每年定期舉行會議，會計師就公司財務狀況、子公司財務及整體運作情形、內控查核情形及關鍵查核事項報告向獨立董事報告，並對財務報告及會計師查核報告或法令修訂有無影響帳列情形溝通；若遇重大事項時得隨時召集會議。</li>
        <li style="list-style: auto;">本公司每年至少召開一次獨立董事與會計師及獨立董事與稽核主管之單獨座談會。</li>
        <li style="list-style: auto;">綜上，獨立董事可透過董事會、審計委員會、單獨座談會以及各種報告及管道(例如：電話、傳真、電子郵件等)，了解公司營運狀況(包括財務業務狀況)及稽核情形，與內部稽核主管及會計師進行良好溝通。</li>
    </ol>
