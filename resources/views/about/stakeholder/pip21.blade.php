<h2 id="pip-21" class="t-main"><i class="i-pip"></i>公司治理 - 董事會成員多元化政策</h2>
<div class="TermBox">
    <p class="mb-3">依據本公司「公司治理實務守則」訂有強化董事會職能之政策，董事會成員組成應考量多元化，並就本身運作、營運型態及發展需求以擬訂適當之多元化方針，宜包括但不限於以下二大面向之標準：<br>
        一、基本條件與價值：性別、年齡、國籍及文化等。<br>
        二、專業知識與技能：專業背景（如法律、會計、產業、財務、行銷或科技）、專業技能及產業經歷等。</p>
    <p class="mb-3">董事會成員宜普遍具備執行職務所必須之知識、技能及素養。為達到公司治理之理想目標，董事會整體應具備之能力如下：<br>
        一、營運判斷能力。<br>
        二、會計及財務分析能力。<br>
        三、經營管理能力。<br>
        四、危機處理能力。<br>
        五、產業知識。<br>
        六、國際市場觀。<br>
        七、領導能力。<br>
        八、決策能力。
    </p>
    <p>本公司董事會共計七席董事，整體董事會所具備之能力符合本公司未來發展所需並落實本公司董事會成員多元化政策，現有本公司落實董事會成員多元化政策之情形。本公司董事成員中獨立董事占比43%、具員工身份之董事占比為43%、女性董事占比為29%，已達成至少一名女性董事目標。董事年齡60歲以上有4名，50~59歲有2名，40~49歲有1名，3席獨立董事連續任期均不超過三屆。
    </p>
